# ---------------------------------------------------------------------------
# Field rename map: XML field names that differ but are semantically identical
# ---------------------------------------------------------------------------

FIELD_RENAMES: dict[str, str] = {
    'op_sel': 'opsel',
    'op_sel_hi': 'opsel_hi',
    'op_sel_hi_2': 'opsel_hi_2',
    'offset': 'ioffset',     # SMEM, MUBUF, FLAT offset → RDNA4 ioffset
    'data': 'vsrc',          # FLAT store source field
    'srsrc': 'rsrc',         # MUBUF buffer resource → VBUFFER rsrc (5b → 9b)
    'addr': 'vaddr',         # FLAT address register → VGLOBAL vaddr
    'data0': 'data0',        # DS data fields — same name, keep explicit
    'data1': 'data1',
}

# Reverse map for looking up src->dst in either direction
_REVERSE_RENAMES: dict[str, str] = {v: k for k, v in FIELD_RENAMES.items()}

# ---------------------------------------------------------------------------
# Encoding format pairing: which source encoding maps to which target
# ---------------------------------------------------------------------------

# Maps (src_enc_name -> dst_enc_name) for CDNA4 -> RDNA4
_CDNA4_TO_RDNA4_ENC_MAP: dict[str, str] = {
    'ENC_SOP1': 'ENC_SOP1',
    'ENC_SOP2': 'ENC_SOP2',
    'ENC_SOPC': 'ENC_SOPC',
    'ENC_SOPK': 'ENC_SOPK',
    'ENC_SOPP': 'ENC_SOPP',
    'ENC_SMEM': 'ENC_SMEM',
    'ENC_VOP1': 'ENC_VOP1',
    'ENC_VOP2': 'ENC_VOP2',
    'ENC_VOPC': 'ENC_VOPC',
    'ENC_VOP3': 'ENC_VOP3',
    'ENC_VOP3P': 'ENC_VOP3P',
    'ENC_DS': 'ENC_VDS',
    'ENC_MUBUF': 'ENC_VBUFFER',
    'ENC_FLAT': 'ENC_VFLAT',
    'ENC_FLAT_GLBL': 'ENC_VGLOBAL',
    'ENC_FLAT_SCRATCH': 'ENC_VSCRATCH',
    'VOP3_SDST_ENC': 'VOP3_SDST_ENC',
}

# ---------------------------------------------------------------------------
# Coherency remap: encodings whose memory-ordering fields require semantic
# translation (GFX940 sc0/sc1/nt -> GFX12 scope/th)
# ---------------------------------------------------------------------------

_COHERENCY_REMAP_ENCODINGS = frozenset({
    'ENC_MUBUF', 'ENC_FLAT', 'ENC_FLAT_GLBL', 'ENC_FLAT_SCRATCH',
})

_GLC_REMAP_ENCODINGS = frozenset({
    'ENC_SMEM',
})

# Fields that participate in coherency remapping (source side)
_COHERENCY_SRC_FIELDS = frozenset({'sc0', 'sc1', 'nt', 'glc'})

# Fields that participate in coherency remapping (target side)
_COHERENCY_DST_FIELDS = frozenset({'scope', 'th'})

# ---------------------------------------------------------------------------
# Field classification
# ---------------------------------------------------------------------------


@dataclass
class FieldMapping:
    """Describes how a single field maps from source to target encoding."""

    kind: str           # 'copy', 'remap', 'drop', 'insert', 'coherency'
    src_name: str       # field name on source ('' for insert)
    dst_name: str       # field name on target ('' for drop)
    src_bits: int       # bit width on source
    dst_bits: int       # bit width on target
    comment: str = ''   # human-readable explanation


@dataclass
class EncodingTranslation:
    """Complete translation recipe for one encoding pair."""

    src_enc_name: str
    dst_enc_name: str
    src_struct: str         # C++ struct name on source
    dst_struct: str         # C++ struct name on target
    src_bit_cnt: int
    dst_bit_cnt: int
    mappings: list[FieldMapping] = field(default_factory=list)
    has_coherency_remap: bool = False
    has_glc_remap: bool = False
    src_dt_index: int = 0       # primary decode table index (for dispatch)
    src_enc_field_bit_cnt: int = 9  # encoding field width in bits
    dst_enc_field_val: int = 0  # actual encoding bitfield value (for dst.encoding)


# ---------------------------------------------------------------------------
# Struct name derivation (matches codegen.py convention)
# ---------------------------------------------------------------------------


def _struct_name(enc_name: str) -> str:
    """Derive the C++ MachineInst struct name from an encoding name.

    Examples:
        ENC_SOP1 -> Sop1MachineInst
        ENC_VOP3P -> Vop3pMachineInst
        ENC_FLAT_GLBL -> FlatGlblMachineInst
        VOP3_SDST_ENC -> Vop3SdstEncMachineInst
        ENC_VDS -> VdsMachineInst
        ENC_VBUFFER -> VbufferMachineInst
        ENC_VFLAT -> VflatMachineInst
    """
    parts = enc_name.split('_')
    if parts[0] == 'ENC':
        parts = parts[1:]
    return ''.join(p.capitalize() for p in parts) + 'MachineInst'


def _dt_index(enc: InstEncoding, spec: IsaSpec) -> int:
    """Extract the primary decode table index for the encoding.

    The primary decode table index encodes the ``encoding`` field value
    shifted left by the don't-care bits. We extract it from the first
    non-negative primary_dt_ptrs entry.  Used for dispatch constants.
    """
    if enc.primary_dt_ptrs is None:
        # Alternate encodings (FLAT_GLBL, etc.) share the parent's DT.
        parent_name = spec.profile.derive_parent_enc_name(enc.enc_name)
        if parent_name in spec.encoding_map:
            parent_enc = spec.encoding_map[parent_name]
            return _dt_index(parent_enc, spec)
        return 0
    for ptr in enc.primary_dt_ptrs:
        if ptr != -1:
            return ptr
    return 0


def _enc_field_value(enc: InstEncoding, spec: IsaSpec) -> int:
    """Extract the actual value of the 'encoding' bitfield constant.

    This is the value written into ``dst.encoding = <value>`` in the
    generated translator functions. Computed as:

        dt_index >> (max_enc_bits - enc_field_bit_cnt)

    Because the DT index is ``enc_value << don't_care_bits``.
    """
    dt_idx = _dt_index(enc, spec)
    max_enc_bits = spec.profile.max_enc_bits
    dont_care = max_enc_bits - enc.enc_field_bit_cnt
    return dt_idx >> dont_care


