"""Auto-generate C++ encoding translator functions for cross-ISA DBT.

For each (src_ISA, dst_ISA) pair, emits C++ functions that translate
instructions by decoding fields from the source ISA's machine_insts.h
bitfield struct and re-encoding into the target ISA's struct.

Fields are classified as:
- COPY:   same name, same width -- bitfield value is copied directly.
- REMAP:  same concept, different XML name -- copied under the new name.
- DROP:   present on source, absent on target -- field is not written.
- INSERT: absent on source, present on target -- set to a default value.

Usage::

    from amdisa import Parser
    from amdisa.isa_profile import CdnaProfile, Rdna4Profile
    from amdisa.encoding_translator_codegen import generate_encoding_translators

    cdna4 = Parser('amdgpu_isa_cdna4.xml', CdnaProfile()).parse()
    rdna4 = Parser('amdgpu_isa_rdna4.xml', Rdna4Profile()).parse()
    generate_encoding_translators(cdna4, rdna4, 'cdna4', 'rdna4', 'output/')
"""

from __future__ import annotations

import shutil
import subprocess
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import TYPE_CHECKING


def _clang_format(path: Path) -> None:
    """Run clang-format -i on path. Silent no-op if clang-format isn't on PATH.

    The codegen emits 4-space indented bodies; project style is 2-space (set
    by the .clang-format at the rocjitsu root). Always running clang-format
    here keeps regenerated files diff-clean even when callers forget to run
    it manually.
    """
    exe = shutil.which('clang-format')
    if not exe:
        print('warning: clang-format not found; emitted files may need manual '
              'formatting', file=sys.stderr)
        return
    subprocess.run([exe, '-i', str(path)], check=True)

if TYPE_CHECKING:
    from amdisa.gpuisa import InstEncoding, IsaSpec

