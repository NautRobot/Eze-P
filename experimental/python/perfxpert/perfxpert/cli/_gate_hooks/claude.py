def _make_hook_entry(script: Path, *, marker: str) -> dict:
    return {
        "hooks": [
            {
                "type": "command",
                "command": str(script),
                "description": f"{marker} — perfxpert tool-priority gate (event-based)",
            }
        ]
    }


def _merge_hook_list(
    existing: Any, new_entry: dict, *, marker: str
) -> list[dict]:
    """Append `new_entry` to an existing hook list, replacing any prior
    perfxpert entry (idempotent reinstall).

    Claude's hook schema accepts a list of entries per event; entries
    without the `matcher` key apply to ALL tools (which is what we
    want — perfxpert gates every non-`mcp__perfxpert__*` call).
    """
    if existing is None:
        return [new_entry]
    if isinstance(existing, list):
        filtered = [e for e in existing if marker not in json.dumps(e)]
        filtered.append(new_entry)
        return filtered
    # Unknown shape — fail safe by wrapping.
    return [existing, new_entry]
