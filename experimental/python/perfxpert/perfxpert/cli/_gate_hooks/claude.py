"""Claude Code gate hook — native PreToolUse surface (Task 4.6).

Per the local Claude hook-surface decision record (Status: DECIDED
(native-pretooluse), 2026-04-18), the Claude gate uses Claude Code's
native `PreToolUse` hook configured inside `.claude/settings.json`.

Install plan:

1. Write a shell script at `<cwd>/.claude/hooks/perfxpert-gate.sh`
   that reads the tool-call JSON on stdin, checks whether
   `<cwd>/.claude/.perfxpert-gate-state.<session_id>.json` exists
   (created by a `PostToolUse` hook on
   `mcp__perfxpert__intent_classify`), and emits either
   `{permissionDecision: "allow"}` or `{permissionDecision:
   "deny", permissionDecisionReason: <retry message>}`.
2. Atomically merge a PreToolUse hook entry AND a PostToolUse hook
   entry into `<cwd>/.claude/settings.json`. Existing hooks are
   preserved; a perfxpert entry on the same hook event is replaced
   (idempotent).
3. The install happens BEFORE MCP registration (I-N1); on failure
   (e.g. existing conflicting hook, unwritable settings.json) raise
   `GateHookUnsupported` so `BackendAdapter.install()` aborts
   before touching `.mcp.json`.

Session-state location (I-N3):
`<cwd>/.claude/.perfxpert-gate-state.<session_id>.json`. A NEW
Claude session starts with a different `session_id` → fresh state
file required → gate engaged.
"""

from __future__ import annotations

import json
import os
import stat
from dataclasses import dataclass
from pathlib import Path
from typing import Any

from perfxpert.cli._backend import _prompt_adapter as pa
from perfxpert.cli._backend.protocol import GateHookUnsupported
from perfxpert.cli._gate_hooks import (
    GATE_HOOK_DISABLED_ENV,
    GATE_REJECTION_REASON_TEMPLATE,
    GATE_STATE_LIFTED_SENTINEL,
)


__all__ = [
    "ClaudeGateHook",
    "ClaudeGateInstallResult",
    "render_gate_script",
    "evaluate_gate_state",
]


# Inline shell script text. Uses `jq` at runtime; if jq is missing,
# falls back to a minimal `python3 -c` path so the hook still works
# in environments without jq installed.
_GATE_SCRIPT = r"""#!/usr/bin/env bash
# Perfxpert PreToolUse gate — generated by perfxpert-code install.
# Reads the claude hook JSON payload on stdin, decides allow/deny.
set -euo pipefail

_payload="$(cat -)"

_perfxpert_safe_sid() {
  case "$1" in
    ""|*[!A-Za-z0-9_-]*)
      return 1
      ;;
  esac
  printf '%s' "$1"
}

# Prefer jq when available; fallback to python for portability.
if command -v jq >/dev/null 2>&1; then
  _tool="$(printf '%s' "$_payload" | jq -r '.tool_name // ""')"
  _sid="$(printf '%s' "$_payload" | jq -r '.session_id // ""')"
else
  _tool="$(printf '%s' "$_payload" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("tool_name",""))')"
  _sid="$(printf '%s' "$_payload" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("session_id",""))')"
fi

if ! _sid="$(_perfxpert_safe_sid "$_sid")"; then
  _sid=""
fi

# Any perfxpert-prefixed tool is always allowed (otherwise the user
# cannot call intent_classify to lift the gate).
case "$_tool" in
  mcp__perfxpert__*)
    printf '%s\n' '{"hookSpecificOutput":{"hookEventName":"PreToolUse","permissionDecision":"allow"}}'
    exit 0
    ;;
esac

_state_file=""
if [ -n "$_sid" ]; then
  _state_file="$(dirname "$0")/../.perfxpert-gate-state.${_sid}.json"
fi
if [ -n "$_state_file" ] && [ -f "$_state_file" ]; then
  if grep -q '__GATE_SENTINEL__' "$_state_file"; then
    printf '%s\n' '{"hookSpecificOutput":{"hookEventName":"PreToolUse","permissionDecision":"allow"}}'
    exit 0
  fi
fi

# Gate engaged — emit deny + reason.
cat <<'JSON'
{"hookSpecificOutput":{"hookEventName":"PreToolUse","permissionDecision":"deny","permissionDecisionReason":"__REJECTION_REASON__"}}
JSON
exit 0
"""


# PostToolUse companion script — writes the state file when
# intent_classify returns.
_POST_SCRIPT = r"""#!/usr/bin/env bash
# Perfxpert PostToolUse gate lift — writes state file once
# mcp__perfxpert__intent_classify returns.
set -euo pipefail

_payload="$(cat -)"

_perfxpert_safe_sid() {
  case "$1" in
    ""|*[!A-Za-z0-9_-]*)
      return 1
      ;;
  esac
  printf '%s' "$1"
}

if command -v jq >/dev/null 2>&1; then
  _tool="$(printf '%s' "$_payload" | jq -r '.tool_name // ""')"
  _sid="$(printf '%s' "$_payload" | jq -r '.session_id // ""')"
else
  _tool="$(printf '%s' "$_payload" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("tool_name",""))')"
  _sid="$(printf '%s' "$_payload" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("session_id",""))')"
fi

if [ "$_tool" = "mcp__perfxpert__intent_classify" ]; then
  if ! _sid="$(_perfxpert_safe_sid "$_sid")"; then
    exit 0
  fi
  _state_file="$(dirname "$0")/../.perfxpert-gate-state.${_sid}.json"
  printf '{"sentinel":"__GATE_SENTINEL__"}\n' > "$_state_file"
fi
# Non-blocking exit.
exit 0
"""


@dataclass(frozen=True)
class ClaudeGateInstallResult:
    settings_path: Path
    hook_script: Path
    post_script: Path


def render_gate_script(classify_tool: str = "mcp__perfxpert__intent_classify") -> str:
    """Return the pretooluse gate shell script body with the rejection
    reason + sentinel substituted in."""
    reason = GATE_REJECTION_REASON_TEMPLATE.format(classify_tool=classify_tool)
    # Escape backslashes / double quotes for safe embedding in the
    # JSON "reason" field (bash heredoc handles the rest).
    reason_escaped = reason.replace("\\", "\\\\").replace('"', '\\"')
    body = _GATE_SCRIPT.replace("__REJECTION_REASON__", reason_escaped)
    body = body.replace("__GATE_SENTINEL__", GATE_STATE_LIFTED_SENTINEL)
    return body


def render_post_script() -> str:
    return _POST_SCRIPT.replace("__GATE_SENTINEL__", GATE_STATE_LIFTED_SENTINEL)


def _settings_path(cwd: Path) -> Path:
    return cwd / ".claude" / "settings.json"


class ClaudeGateHook:
    """Installer for the Claude PreToolUse gate hook."""

    #: Marker substring we write into the hook entry so the uninstall
    #: path can identify our block without parsing anything.
    MARKER_SUBSTRING = "perfxpert-gate"

    def install(
        self,
        cwd: Path,
        *,
        classify_tool: str = "mcp__perfxpert__intent_classify",
    ) -> ClaudeGateInstallResult:
        """Atomically add the PreToolUse + PostToolUse hook entries.

        Raises `GateHookUnsupported` BEFORE MCP registration runs if
        the settings.json cannot be safely merged (I-N1).
        """
        if os.environ.get(GATE_HOOK_DISABLED_ENV, "").strip() == "0":
            raise GateHookUnsupported(
                f"{GATE_HOOK_DISABLED_ENV}=0 — gate hook install skipped "
                "by user request"
            )

        cwd = Path(cwd).expanduser().resolve()
        settings = _settings_path(cwd)
        script = cwd / ".claude" / "hooks" / "perfxpert-gate.sh"
        post_script = cwd / ".claude" / "hooks" / "perfxpert-gate-post.sh"

        script.parent.mkdir(parents=True, exist_ok=True)

        # Write the gate scripts (executable).
        pa.atomic_write(script, render_gate_script(classify_tool))
        pa.atomic_write(post_script, render_post_script())
        for p in (script, post_script):
            try:
                p.chmod(p.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP)
            except OSError:
                # Some filesystems ignore chmod (WSL/Windows); the
                # hook will then rely on `bash <script>` invocation.
                pass

        # Merge PreToolUse + PostToolUse entries into settings.json.
        try:
            self._merge_settings(settings, script, post_script)
        except GateHookUnsupported:
            # Clean up the script we wrote — don't leave partial
            # state behind.
            script.unlink(missing_ok=True)
            post_script.unlink(missing_ok=True)
            raise

        return ClaudeGateInstallResult(
            settings_path=settings,
            hook_script=script,
            post_script=post_script,
        )

    def _merge_settings(
        self, settings: Path, pre_script: Path, post_script: Path
    ) -> None:
        settings.parent.mkdir(parents=True, exist_ok=True)
        existing: dict[str, Any] = {}
        if settings.is_file():
            try:
                existing = json.loads(settings.read_text())
            except json.JSONDecodeError as exc:
                raise GateHookUnsupported(
                    f"{settings} is not valid JSON; refusing to merge ({exc})"
                )
            if not isinstance(existing, dict):
                raise GateHookUnsupported(
                    f"{settings} top-level JSON must be an object"
                )

        hooks = existing.setdefault("hooks", {})
        if not isinstance(hooks, dict):
            raise GateHookUnsupported(
                f"{settings}['hooks'] must be an object"
            )

        pre_entry = _make_hook_entry(pre_script, marker=self.MARKER_SUBSTRING)
        post_entry = _make_hook_entry(post_script, marker=self.MARKER_SUBSTRING)

        hooks["PreToolUse"] = _merge_hook_list(
            hooks.get("PreToolUse"), pre_entry, marker=self.MARKER_SUBSTRING
        )
        hooks["PostToolUse"] = _merge_hook_list(
            hooks.get("PostToolUse"), post_entry, marker=self.MARKER_SUBSTRING
        )

        pa.atomic_write(settings, json.dumps(existing, indent=2) + "\n")

    def uninstall(self, cwd: Path) -> bool:
        """Remove the PreToolUse + PostToolUse entries from settings.json.

        Returns True iff the removal succeeded (or the entries were
        already absent). Returns False when settings.json exists but
        cannot be parsed (drift).
        """
        cwd = Path(cwd).expanduser().resolve()
        settings = _settings_path(cwd)
        if not settings.is_file():
            return True

        try:
            data = json.loads(settings.read_text())
        except json.JSONDecodeError:
            return False

        hooks = data.get("hooks")
        if not isinstance(hooks, dict):
            return True
        for event in ("PreToolUse", "PostToolUse"):
            entries = hooks.get(event)
            if not entries:
                continue
            hooks[event] = [
                e
                for e in entries
                if self.MARKER_SUBSTRING not in json.dumps(e)
            ]
            if not hooks[event]:
                hooks.pop(event)
        if not hooks:
            data.pop("hooks", None)

        pa.atomic_write(settings, json.dumps(data, indent=2) + "\n")

        # Remove the scripts too.
        for script in (
            cwd / ".claude" / "hooks" / "perfxpert-gate.sh",
            cwd / ".claude" / "hooks" / "perfxpert-gate-post.sh",
        ):
            try:
                script.unlink()
            except OSError:
                pass

        return True


# ---------------------------------------------------------------------------
# Pure evaluator used by unit tests (mirrors the shell script logic).
# ---------------------------------------------------------------------------


def evaluate_gate_state(
    tool_name: str,
    *,
    intent_classify_observed: bool,
    classify_tool: str = "mcp__perfxpert__intent_classify",
) -> dict[str, Any]:
    """Return the JSON payload the PreToolUse hook would emit.

    Hoisted into Python so unit tests can exercise the rules without
    shelling out to bash. The shell script mirrors this logic
    line-for-line; a drift between the two would be a bug.
    """
    if tool_name.startswith("mcp__perfxpert__"):
        return {
            "hookSpecificOutput": {
                "hookEventName": "PreToolUse",
                "permissionDecision": "allow",
            }
        }
    if intent_classify_observed:
        return {
            "hookSpecificOutput": {
                "hookEventName": "PreToolUse",
                "permissionDecision": "allow",
            }
        }
    return {
        "hookSpecificOutput": {
            "hookEventName": "PreToolUse",
            "permissionDecision": "deny",
            "permissionDecisionReason": GATE_REJECTION_REASON_TEMPLATE.format(
                classify_tool=classify_tool
            ),
        }
    }


